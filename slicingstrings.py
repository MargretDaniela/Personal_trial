#slicing
b= "Hello, World!"
print(b[2:5])#oll

#slice from the start
b = "Hello, World!"
print(b[:5])#Hello

#slice to the end
b = "Hello, World!"
print(b[2:])#llo, World

#negative indexing
b = "Hello, World!"
print(b[-5:-2])#orl

#modify strings
#upper()
a = "Hello, World!"
print(a.upper())#HELLO, WORLD


#lower()
a = "Hello, World!"
print(a.lower())#hello, world

#strip
a = "Hello, World!"
print(a.strip())#Hello, World

#replace
a = "Hello, World!"
print(a.replace("H", "J"))#Jello, World

#split string
a = "Hello, World!"
print(a.split(","))


