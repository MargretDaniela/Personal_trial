#Naming conventions
#1 snake case(no space all in lower case withnan underscore to seperart the words)
student_name="Daniela"
student_reg_number="3440/DSC/789"
print(student_name,student_reg_number)
      
#camel case
#the second letters of the word start with a capital letter)
studentName="Jesca"
studentRegNumber="455/DSC/897"
print(studentName , studentRegNumber)

#pascals case
#first letters start with no underscore
StudentName="maggie"
StudentRegNumber="64848/TGF/890"
